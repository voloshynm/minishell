#include "libft.h"

/*
* The ft_isalnum function checks if a character is alphanumeric.
*/
int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57))
		return (1);
	return (0);
}
